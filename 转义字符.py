# 制表符
# 在程序中，使用\t可以在控制台输出一个制表符，协助在输出文本时对齐
print('W\t*')
print("WW\t*")
print("WWW\t*")

# /n 换行
print("这是第一行\n这是第二行 \n这是第三行")


# \\
print("C:\\windows\\system32\\cmd.exe")

